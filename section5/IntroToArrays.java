public class IntroToArrays {
    public static void main(String[] args) {
        String [] kingdoms = {"Mercia","Wessex","Northumbria","East Anglia"};
        System.out.println(kingdoms[0]);
        System.out.println(kingdoms[1]);
        System.out.println(kingdoms[2]);
        System.out.println(kingdoms[3]);

        String [] students = {"Harry", "Neville","Ron","Hermione"};
        System.out.println(students[0] + " is going to take seat 0");
        System.out.println(students[1] + " is going to take seat 1");
        System.out.println(students[2] + " is going to take seat 2");
        System.out.println(students[3] + " is going to take seat 3");

       
    }
}